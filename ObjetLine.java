/*Cette classe nous a permis de analizer si notre graphe etait bon, avec un objet line, on peut iniilizer toutes les lignes crees a partir 
 * de la liste des aretes avec distances*/

public class ObjetLine{
	protected int x1,x2,y1,y2;
	public ObjetLine(int x1, int y1, int x2, int y2){
		this.x1 = x1; 
		this.x2 = x2; 
		this.y1 = y1; 
		this.y2 = y2; 
	}
}
